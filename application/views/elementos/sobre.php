    <p class="card-text">Em 1947, Eva Herz, filha de imigrantes judeus da Alemanha, montou um serviço de aluguel de livros chamado Biblioteca Circulante em sua 
    casa na Alameda Lorena, em São Paulo. Os livros eram importados da Europa e sua clientela era principalmente de imigrantes.<br> O serviço se popularizou e a biblioteca circulante passou a emprestar livros de autores brasileiros, bem como a vendê-los.
Em 1969, Herz inaugura a loja do Conjunto Nacional, na Avenida Paulista, extinguindo a Biblioteca Circulante. O filho de Eva, Pedro Herz, tornou-se seu sócio na empresa.<br> Na comemoração de seus 60 anos, em 2007, a Livraria Nobel renovou sua identidade visual e também sua visão como empresa. A unidade da Avenida Paulista foi reinaugurada em 21 de maio de 2007, no espaço antes ocupado pelo histórico Cine Astor, substituindo as quatro lojas menores existentes no Conjunto Nacional.
